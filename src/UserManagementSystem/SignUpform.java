    private void signbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signbtnActionPerformed
        String email=emailField.getText();
        String username=usernameField.getText();
        String password=new String(passwordField.getPassword());
        String dob=dobField.getText();
        if( username.isEmpty() || password.isEmpty()||Validation.isvalidEmail(email)==false||Validation.isValidDateOfBirth(dob)==false){
            if(username.isEmpty() || password.isEmpty()){
            Message m=new Message(this,true,"invalid ,cant be empty");
            m.setVisible(true);
        }if(Validation.isvalidEmail(email)==false){
            Message m=new Message(this,true,"Invalid: Email Address Can't Be Empty");
            m.setVisible(true);
        }
       if(Validation.isValidDateOfBirth(dob)==false){
           Message m=new Message(this,true,"Date of birth is invalid");
            m.setVisible(true);
       }
    }else{
       User u=new User(email,username,password,dob);
       u.setStatus("Offline");
       Message m=new Message(this,true,"Successfully signed in");
            m.setVisible(true);
       u.signup();

       Firstpage f=new Firstpage();
       f.setTitle("Signup or login");
       f.setVisible(true);
       dispose();

    }
        
    }//GEN-LAST:event_signbtnActionPerformed

