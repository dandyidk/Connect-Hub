    private void showFriendRequests(int iterator){
        String text = "";
        try{
            if(((String)(((JSONObject)friendRequestsArray.get(iterator)).get("Request Status"))).compareTo("Pending")==0){
        text = text + ((String)(((JSONObject)friendRequestsArray.get(iterator)).get("Profile Username")))+"\n\n";
            }
        }catch(IndexOutOfBoundsException e){}
        jLabel1.setText(text);
    }
    
    private void AcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptActionPerformed
        JSONObject fr =(JSONObject) (this.friendRequestsArray.get(this.iterator));//get friend request
        FriendRequests friendRequests =new FriendRequests((String)fr.get("Request Status"), (String)fr.get("Profile Id"), user.getUserId());
        friendRequests.acceptFriendRequestStatus();
        Message m = new Message(null,true,"Friend Request accepted");
        m.setLocationRelativeTo(this);
        m.setVisible(true);
    }//GEN-LAST:event_AcceptActionPerformed

    private void RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveActionPerformed
        JSONObject fr =(JSONObject) (this.friendRequestsArray.get(this.iterator));//get friend request
        FriendRequests friendRequests =new FriendRequests((String)fr.get("Request Status"), (String)fr.get("Profile Id"), user.getUserId());
        friendRequests.removeFriendRequestStatus();
        Message m = new Message(null,true,"Friend Request Removed");
        m.setLocationRelativeTo(this);
        m.setVisible(true);
    }//GEN-LAST:event_RemoveActionPerformed

    private void BackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BackActionPerformed
        if(this.friendRequestsArray.size()!=0){
        this.iterator = (this.iterator-1)%this.friendRequestsArray.size();
        showFriendRequests(this.iterator);
        }
    }//GEN-LAST:event_BackActionPerformed

    private void NextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextActionPerformed
        if(this.friendRequestsArray.size()!=0){
        this.iterator = (this.iterator+1)%this.friendRequestsArray.size();
        showFriendRequests(this.iterator);
        }
    }//GEN-LAST:event_NextActionPerformed

